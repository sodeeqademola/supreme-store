type ProductsProps = {
  name: string;
  id: string;
  createdAt: Date;
  updatedAT: Date;
  description: string;
  price: number;
  brand: string;
  category: string;
  inStock: boolean;
  images: { color: string; colorCode: string | null; image: string }[];
};

