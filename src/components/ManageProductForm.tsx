"use client";
import React, { SVGProps, useEffect, useState } from "react";

import {
  Table,
  TableHeader,
  TableColumn,
  TableBody,
  TableRow,
  TableCell,
  Input,
  Button,
  DropdownTrigger,
  Dropdown,
  DropdownMenu,
  DropdownItem,
  User,
  Pagination,
  Selection,
  SortDescriptor,
} from "@heroui/react";
import { motion } from "framer-motion";
import dynamic from "next/dynamic";
import { deleteProduct, getProductsForAdmin } from "@/Action/paymentIntent";
